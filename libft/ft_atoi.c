int	ft_atoi(const char *str)
{
	int	i;
	int	neg;
	int	res;

	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	neg = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if ((str[i + 1] == '-') || (str[i + 1] == '+'))
			return (0);
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - 48);
		i++;
	}
	return (res * neg);
}
